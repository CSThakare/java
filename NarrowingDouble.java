class NarrowingDouble
{
	public static void main(String[] args) 
	{
	 	double d=3.4;
		float f=(float)d;
		System.out.println(f);
		long l=(long)d;
		System.out.println(l);
		int i=(int)d;
		System.out.println(i);
		short s=(short)d;
		System.out.println(s);
		byte b=(byte)d;
		System.out.println(b);
		//char ch=(char)d;
		//System.out.println(ch);
	}
}
